entry1 = float(input("masukkan angka ke-1: "))
operator = input("masukkan operator: ")
entry2 = float(input("masukkan angka ke-2: "))


if operator == "+":
    print( entry1 + entry2)
elif operator == "-":
    print( entry1 - entry2)
elif operator == "*":
    print(entry1 * entry2)
elif operator == "/":
    print(entry1 / entry2)
else:
    print("operator salah")







